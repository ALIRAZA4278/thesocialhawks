// Helper function to get job by slug
export const getJobBySlug = (slug) => {
  return jobsData.find(job => job.slug === slug);
};

// Helper function to get featured jobs
export const getFeaturedJobs = () => {
  return jobsData.filter(job => job.featured);
};

// Helper function to get jobs by department
export const getJobsByDepartment = (department) => {
  return jobsData.filter(job => job.department === department);
};

// Helper function to get all departments
export const getDepartments = () => {
  return [...new Set(jobsData.map(job => job.department))];
};